-- Función: Generar reporte ejecutivo completo
CREATE OR REPLACE FUNCTION generar_reporte_ejecutivo()
RETURNS JSON AS $$
DECLARE
    reporte JSON;
BEGIN
    SELECT json_build_object(
        'fecha', CURRENT_DATE,
        'periodo', 'Semana ' || TO_CHAR(CURRENT_DATE, 'WW/YYYY'),
        
        'kpi_throughput', json_build_object(
            'valor', (SELECT m1_throughput_total_entregas FROM vw_managerial_kpis_weekly),
            'meta', 'Incremental'
        ),
        
        'kpi_lead_time', json_build_object(
            'valor_horas', (SELECT m2_lead_time_promedio_h FROM vw_managerial_kpis_weekly),
            'meta_horas', 24,
            'cumple', (SELECT m2_lead_time_promedio_h FROM vw_managerial_kpis_weekly) <= 24
        ),
        
        'kpi_project_mix', json_build_object(
            'top_proyecto', (SELECT m3_top_proyecto FROM vw_managerial_kpis_weekly),
            'porcentaje', (SELECT m3_top_proyecto_pct FROM vw_managerial_kpis_weekly)
        ),
        
        'kpi_calidad', json_build_object(
            'rework_rate', (SELECT m4_tasa_retrabajo_pct FROM vw_managerial_kpis_weekly),
            'meta', 10,
            'cumple', (SELECT m4_tasa_retrabajo_pct FROM vw_managerial_kpis_weekly) < 10
        ),
        
        'kpi_utilizacion', json_build_object(
            'tasa', (SELECT m5_utilizacion_equipo_pct FROM vw_managerial_kpis_weekly),
            'meta', 85,
            'cumple', (SELECT m5_utilizacion_equipo_pct FROM vw_managerial_kpis_weekly) >= 85
        )
    ) INTO reporte;
    
    RETURN reporte;
END;
$$ LANGUAGE plpgsql;

-- Función: Calcular score de desarrollador
CREATE OR REPLACE FUNCTION calcular_score_desarrollador(dev_id UUID)
RETURNS JSON AS $$
DECLARE
    score JSON;
    issues_count INT;
    avg_lead FLOAT;
    dias_inactivo INT;
BEGIN
    -- Obtener métricas del desarrollador (última semana)
    SELECT 
        COUNT(*),
        AVG(lead_time_hours)
    INTO issues_count, avg_lead
    FROM github_issues
    WHERE developer_id = dev_id
    AND state = 'closed'
    AND closed_at >= CURRENT_DATE - INTERVAL '7 days';
    
    -- Obtener días inactivos
    SELECT days_inactive INTO dias_inactivo
    FROM daily_metrics
    WHERE developer_id = dev_id
    AND date = CURRENT_DATE;
    
    -- Construir score
    SELECT json_build_object(
        'issues_cerrados', COALESCE(issues_count, 0),
        'lead_time_promedio', COALESCE(avg_lead, 0),
        'dias_inactivo', COALESCE(dias_inactivo, 0),
        'score_velocidad', CASE WHEN avg_lead < 24 THEN 100 ELSE GREATEST(0, 100 - (avg_lead - 24) * 2) END,
        'score_productividad', LEAST(100, issues_count * 10)
    ) INTO score;
    
    RETURN score;
END;
$$ LANGUAGE plpgsql;

