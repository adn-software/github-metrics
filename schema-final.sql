-- Habilitar extensión para UUIDs
CREATE EXTENSION IF NOT EXISTS "uuid-ossp";

-- ============================================================================
-- 1. TABLA DE DESARROLLADORES
-- ============================================================================

CREATE TABLE IF NOT EXISTS developers (
    id UUID PRIMARY KEY DEFAULT uuid_generate_v4(),
    github_username TEXT UNIQUE NOT NULL,
    full_name TEXT NOT NULL,
    is_active BOOLEAN DEFAULT TRUE,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- ============================================================================
-- 2. TABLA DE MÉTRICAS DIARIAS (Músculo y Disponibilidad)
-- ============================================================================

CREATE TABLE IF NOT EXISTS daily_metrics (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    developer_id UUID REFERENCES developers(id) ON DELETE CASCADE,
    date DATE NOT NULL,
    commits_count INT DEFAULT 0,
    lines_added INT DEFAULT 0,
    lines_deleted INT DEFAULT 0,
    repos_touched INT DEFAULT 0,
    last_activity_at TIMESTAMP WITH TIME ZONE,
    days_inactive INT DEFAULT 0,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
    UNIQUE(developer_id, date) -- Evita duplicados diarios por dev
);

-- ============================================================================
-- 3. TABLA DE ISSUES (Velocidad, Entrega y Retrabajo)
-- ============================================================================

CREATE TABLE IF NOT EXISTS github_issues (
    github_issue_id BIGINT PRIMARY KEY, -- El ID real que viene de GitHub
    developer_id UUID REFERENCES developers(id) ON DELETE SET NULL,
    repository_name TEXT NOT NULL,
    title TEXT NOT NULL,
    state TEXT NOT NULL CHECK (state IN ('open', 'closed')),
    labels TEXT[] DEFAULT '{}', -- Arreglo de etiquetas (ej. ['bug', 'feature', 'rework'])
    created_at TIMESTAMP WITH TIME ZONE NOT NULL,
    closed_at TIMESTAMP WITH TIME ZONE,
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
    lead_time_hours FLOAT GENERATED ALWAYS AS (
        EXTRACT(EPOCH FROM (closed_at - created_at)) / 3600
    ) STORED
);

-- ============================================================================
-- 4. TABLA DE ACTIVIDAD POR PROYECTO (Para Project Mix)
-- ============================================================================

CREATE TABLE IF NOT EXISTS project_activity (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    date DATE NOT NULL,
    repo_name TEXT NOT NULL,
    commits_count INT DEFAULT 0,
    issues_count INT DEFAULT 0,
    developers_count INT DEFAULT 0,
    lines_changed INT DEFAULT 0,
    project_priority TEXT CHECK (project_priority IN ('high', 'medium', 'low')),
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
    UNIQUE(date, repo_name)
);

-- ============================================================================
-- 5. TABLA DE LOGS DE EXTRACCIÓN
-- ============================================================================

CREATE TABLE IF NOT EXISTS extraction_logs (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    extraction_date TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
    status TEXT,
    developers_processed INT,
    issues_processed INT,
    records_inserted INT,
    records_updated INT,
    errors TEXT,
    duration_seconds INT
);

