-- VISTA M1: Throughput Semanal
CREATE OR REPLACE VIEW vw_kpi_throughput_semanal AS
WITH semanas AS (
    SELECT 
        DATE_TRUNC('week', closed_at)::DATE as semana,
        COUNT(*) as total_issues_cerrados,
        COUNT(DISTINCT developer_id) as devs_activos,
        ROUND(AVG(lead_time_hours)::numeric, 2) as avg_lead_time
    FROM github_issues
    WHERE state = 'closed' 
    AND closed_at >= CURRENT_DATE - INTERVAL '8 weeks'
    AND closed_at IS NOT NULL
    GROUP BY DATE_TRUNC('week', closed_at)::DATE
)
SELECT 
    semana,
    total_issues_cerrados,
    devs_activos,
    avg_lead_time,
    ROUND(
        (((total_issues_cerrados::FLOAT / NULLIF(LAG(total_issues_cerrados) OVER (ORDER BY semana), 0)) - 1) * 100)::numeric,
        2
    ) as variacion_porcentual
FROM semanas
ORDER BY semana DESC;

-- VISTA M2: Lead Time Tendencia
CREATE OR REPLACE VIEW vw_kpi_lead_time_tendencia AS
SELECT 
    DATE_TRUNC('week', closed_at)::DATE as semana,
    ROUND(AVG(lead_time_hours)::numeric, 2) as lead_time_promedio_h,
    MIN(lead_time_hours) as mejor_lead_time_h,
    MAX(lead_time_hours) as peor_lead_time_h,
    COUNT(*) as total_entregas,
    ROUND((AVG(lead_time_hours) / 24.0)::numeric, 2) as lead_time_dias
FROM github_issues
WHERE state = 'closed' 
AND closed_at >= CURRENT_DATE - INTERVAL '8 weeks'
AND closed_at IS NOT NULL
AND lead_time_hours > 0
GROUP BY DATE_TRUNC('week', closed_at)::DATE
ORDER BY semana DESC;

-- VISTA M3: Distribución de Esfuerzo (Project Mix)
CREATE OR REPLACE VIEW vw_project_mix_weekly AS
SELECT 
    repository_name,
    COUNT(*) as total_issues_closed,
    ROUND((COUNT(*) * 100.0 / SUM(COUNT(*)) OVER ())::numeric, 2) as percentage_of_effort,
    COUNT(DISTINCT developer_id) as developers_involved
FROM github_issues
WHERE state = 'closed' 
AND closed_at >= DATE_TRUNC('week', NOW())
AND closed_at IS NOT NULL
GROUP BY repository_name
ORDER BY total_issues_closed DESC;

-- VISTA M4: Tasa de Retrabajo
CREATE OR REPLACE VIEW vw_kpi_rework_rate AS
WITH issues_semana AS (
    SELECT 
        COUNT(*) as total_issues,
        SUM(CASE WHEN 'bug' = ANY(labels) OR 'rework' = ANY(labels) THEN 1 ELSE 0 END) as issues_rework
    FROM github_issues
    WHERE state = 'closed'
    AND closed_at >= DATE_TRUNC('week', NOW())
    AND closed_at IS NOT NULL
)
SELECT 
    total_issues,
    issues_rework,
    ROUND(((issues_rework::FLOAT / NULLIF(total_issues, 0)) * 100)::numeric, 2) as rework_rate_porcentaje,
    CASE 
        WHEN (issues_rework::FLOAT / NULLIF(total_issues, 0)) * 100 < 5 THEN '🟢 EXCELENTE'
        WHEN (issues_rework::FLOAT / NULLIF(total_issues, 0)) * 100 < 10 THEN '🟡 ACEPTABLE'
        ELSE '🔴 REQUIERE ATENCIÓN'
    END as calidad_status
FROM issues_semana;

-- VISTA M5: Utilización del Equipo
CREATE OR REPLACE VIEW vw_kpi_utilizacion_equipo AS
WITH estado_devs AS (
    SELECT 
        d.id,
        d.github_username,
        d.full_name,
        COALESCE(dm.days_inactive, 999) as dias_inactivo,
        CASE 
            WHEN COALESCE(dm.days_inactive, 999) <= 1 THEN 'ACTIVO'
            WHEN COALESCE(dm.days_inactive, 999) <= 3 THEN 'PARCIAL'
            ELSE 'BENCH'
        END as estado
    FROM developers d
    LEFT JOIN daily_metrics dm ON d.id = dm.developer_id AND dm.date = CURRENT_DATE
    WHERE d.is_active = TRUE
)
SELECT 
    COUNT(*) as total_desarrolladores,
    SUM(CASE WHEN estado = 'ACTIVO' THEN 1 ELSE 0 END) as devs_activos,
    SUM(CASE WHEN estado = 'PARCIAL' THEN 1 ELSE 0 END) as devs_parciales,
    SUM(CASE WHEN estado = 'BENCH' THEN 1 ELSE 0 END) as devs_bench,
    ROUND(((SUM(CASE WHEN estado = 'ACTIVO' THEN 1 ELSE 0 END)::FLOAT / NULLIF(COUNT(*), 0)) * 100)::numeric, 2) as tasa_utilizacion
FROM estado_devs;

-- VISTA GERENCIAL CONSOLIDADA: Gran Tablero Semanal (KPIs Consolidados)
CREATE OR REPLACE VIEW vw_managerial_kpis_weekly AS
WITH weekly_issues AS (
    SELECT * FROM github_issues 
    WHERE state = 'closed' 
    AND closed_at >= DATE_TRUNC('week', NOW())
    AND closed_at IS NOT NULL
),
active_devs AS (
    SELECT COUNT(*) as total_devs FROM developers WHERE is_active = TRUE
),
benched_devs AS (
    SELECT COUNT(*) as benched_count FROM vw_bench_watch WHERE days_inactive > 2
)
SELECT 
    -- M1: Throughput
    (SELECT COUNT(*) FROM weekly_issues) AS m1_throughput_total_entregas,
    
    -- M2: Lead Time
    COALESCE((SELECT ROUND(AVG(lead_time_hours)::numeric, 2) FROM weekly_issues), 0) AS m2_lead_time_promedio_h,
    
    -- M3: Project Mix (Top repo)
    (SELECT repository_name FROM vw_project_mix_weekly LIMIT 1) as m3_top_proyecto,
    (SELECT percentage_of_effort FROM vw_project_mix_weekly LIMIT 1) as m3_top_proyecto_pct,
    
    -- M4: Tasa de Retrabajo
    COALESCE(
        ROUND(((SELECT COUNT(*) FROM weekly_issues WHERE 'bug' = ANY(labels) OR 'rework' = ANY(labels)) * 100.0 / NULLIF((SELECT COUNT(*) FROM weekly_issues), 0))::numeric, 2), 
    0) AS m4_tasa_retrabajo_pct,
    
    -- M5: Utilización / Bench Rate
    COALESCE(
        ROUND(((1.0 - (SELECT benched_count FROM benched_devs)::FLOAT / NULLIF((SELECT total_devs FROM active_devs), 0)) * 100)::numeric, 2), 
    0) AS m5_utilizacion_equipo_pct;

