-- VISTA O1: Radar de Disponibilidad (Bench Watch)
CREATE OR REPLACE VIEW vw_bench_watch AS
SELECT 
    d.id as developer_id,
    d.full_name, 
    d.github_username,
    dm.days_inactive, 
    dm.last_activity_at,
    CASE 
        WHEN dm.days_inactive > 2 THEN '🔴 CRÍTICO'
        WHEN dm.days_inactive = 2 THEN '🟠 ALTO'
        WHEN dm.days_inactive = 1 THEN '🟡 MEDIO'
        ELSE '🟢 ACTIVO'
    END as status_bench
FROM developers d
LEFT JOIN daily_metrics dm ON d.id = dm.developer_id AND dm.date = CURRENT_DATE
WHERE d.is_active = TRUE
ORDER BY dm.days_inactive DESC NULLS LAST;

-- VISTA O2: Velocidad de Entrega (Throughput Diario)
CREATE OR REPLACE VIEW vw_throughput_daily AS
SELECT 
    DATE(closed_at) as date,
    COUNT(*) as issues_closed,
    ROUND(AVG(lead_time_hours)::numeric, 2) as avg_lead_time_h,
    COUNT(DISTINCT developer_id) as developers_activos
FROM github_issues
WHERE state = 'closed' 
AND closed_at >= CURRENT_DATE - INTERVAL '30 days'
AND closed_at IS NOT NULL
GROUP BY DATE(closed_at)
ORDER BY date DESC;

-- VISTA O3: Eficiencia por Desarrollador (Lead Time)
CREATE OR REPLACE VIEW vw_velocity_by_dev AS
SELECT 
    d.id as developer_id,
    d.github_username,
    d.full_name,
    COUNT(i.github_issue_id) as total_issues_closed,
    ROUND(AVG(i.lead_time_hours)::numeric, 2) as avg_lead_time_h,
    MIN(i.lead_time_hours) as best_lead_time_h,
    MAX(i.closed_at) as last_delivery
FROM developers d
LEFT JOIN github_issues i ON d.id = i.developer_id 
    AND i.state = 'closed' 
    AND i.closed_at >= CURRENT_DATE - INTERVAL '7 days'
WHERE d.is_active = TRUE
GROUP BY d.id, d.github_username, d.full_name
ORDER BY avg_lead_time_h ASC NULLS LAST;

-- VISTA O4: Volumen de Código (Code Volume)
CREATE OR REPLACE VIEW vw_code_volume_daily AS
SELECT 
    date,
    SUM(lines_added) as total_added,
    SUM(lines_deleted) as total_deleted,
    SUM(lines_added + lines_deleted) as total_churn,
    ROUND(AVG(lines_added::FLOAT / NULLIF(lines_deleted, 0))::numeric, 2) as add_delete_ratio
FROM daily_metrics
WHERE date >= CURRENT_DATE - INTERVAL '30 days'
GROUP BY date
ORDER BY date DESC;

-- VISTA O5: Índice de Foco (Context Switching)
CREATE OR REPLACE VIEW vw_focus_index AS
SELECT 
    d.github_username,
    d.full_name,
    dm.date,
    dm.repos_touched,
    dm.lines_added + dm.lines_deleted as code_volume,
    CASE 
        WHEN dm.repos_touched = 1 THEN '🟢 ENFOCADO'
        WHEN dm.repos_touched = 2 THEN '🟡 MODERADO'
        ELSE '🔴 DISPERSO'
    END as focus_status
FROM daily_metrics dm
JOIN developers d ON dm.developer_id = d.id
WHERE dm.date >= CURRENT_DATE - INTERVAL '7 days'
AND d.is_active = TRUE
ORDER BY dm.date DESC, dm.repos_touched DESC;

-- VISTA O6: Consistencia de Flujo (Commit Pulse)
CREATE OR REPLACE VIEW vw_commit_pulse AS
SELECT 
    d.id as developer_id,
    d.github_username,
    dm.date,
    dm.commits_count,
    LAG(dm.commits_count, 1) OVER (PARTITION BY d.id ORDER BY dm.date) as commits_prev_day,
    dm.commits_count - LAG(dm.commits_count, 1) OVER (PARTITION BY d.id ORDER BY dm.date) as commit_delta
FROM daily_metrics dm
JOIN developers d ON dm.developer_id = d.id
WHERE dm.date >= CURRENT_DATE - INTERVAL '14 days'
AND d.is_active = TRUE
ORDER BY d.github_username, dm.date DESC;

-- VISTA O7: Alerta de Tareas Estancadas (Stale Issues)
CREATE OR REPLACE VIEW vw_stale_issues AS
SELECT 
    i.github_issue_id,
    i.repository_name,
    i.title,
    d.full_name AS assignee,
    i.labels,
    EXTRACT(DAY FROM (NOW() - i.created_at))::INT AS days_open,
    i.created_at,
    CASE 
        WHEN EXTRACT(DAY FROM (NOW() - i.created_at)) > 7 THEN '🔴 CRÍTICO'
        WHEN EXTRACT(DAY FROM (NOW() - i.created_at)) > 3 THEN '🟠 URGENTE'
        ELSE '🟡 ATENCIÓN'
    END as urgency_level
FROM github_issues i
LEFT JOIN developers d ON i.developer_id = d.id
WHERE i.state = 'open' AND i.created_at < NOW() - INTERVAL '3 days'
ORDER BY days_open DESC;

